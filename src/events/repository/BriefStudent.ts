/**
 * Represents a brief student.
 *
 * @interface BriefStudent
 * @property {string} id - The ID of the student.
 * @property {string} name - The name of the student.
 * @property {string} email - The email of the student.
 * @property {string} phone - The phone number of the student.
 */
interface BriefStudent {
  id: string;
  name: string;
  email: string;
  phone: string;
}
